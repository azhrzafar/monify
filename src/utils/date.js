export default function getMonthYear() {
  const monthList = [
    "January",
    "February",
    "March",
    "April",
    "May",
    "June",
    "July",
    "August",
    "September",
    "October",
    "November",
    "December"
  ];
  const date = new Date();
  return monthList[date.getMonth()] + " " + date.getFullYear();
}
